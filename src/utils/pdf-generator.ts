export interface ShiftData {
  show_name: string;
  role: string;
  date: string;
  time: string;
}

export interface VolunteerData {
  name: string;
  email: string | null;
}

interface VolunteerRecord {
  id: string; // UUID
  name: string;
  email?: string;
  phone?: string;
  approved: boolean;
}

interface ShiftRow {
  id: number;
  show_id: number;
  show_name: string;
  show_date_id: number;
  role: string;
  arrive_time: string;
  depart_time: string;
  show_date: string;
  start_time: string;
  end_time: string;
}

export interface PDFData {
  volunteer: VolunteerRecord;
  assignedShifts: ShiftRow[];
  availableShifts: ShiftRow[];
  generatedAt: string;
}

export function generateShiftRemovalPDF(volunteer: VolunteerData, shifts: ShiftData[]): Uint8Array {
  const content = generatePDFContent(volunteer, shifts);

  // For a simple implementation, we'll return a plain text file
  // In production, you might want to use a library like jsPDF or puppeteer
  return new TextEncoder().encode(content);
}

